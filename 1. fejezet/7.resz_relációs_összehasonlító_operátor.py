# <, >, <=, >=, !=, ==

szam1 = 10
szam2 = 4

print(szam1 < szam2) #False, szam1 kissebb szam2-vel?

print(szam1 > szam2) #True, szam1 nagyobb szam2-vel?

print(szam1 <= szam2) #False, szam1 kissebb vagy egyenlő szam2-vel?

print(szam1 >= szam2) #True, szam1 nagyobb vagy egyenlő szam2-vel?

print(szam1 != szam2) #True, szam1 nem egyenlő szam2-vel?

print(szam1 == szam2) #False, szam1 egyenlő szam2-vel?