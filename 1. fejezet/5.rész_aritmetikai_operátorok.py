# +, -, *, /, //, **, %

szam1 = 5
szam2 = 10
szam3 = 11

print(szam1 + szam2)
print(szam1 - szam2)
print(szam1 * szam2)
print(szam2 / szam1)
print(szam2 // szam1) # Egész számot ír ki 5 / 10 = 0.5 de 5 // 10 = 0
print(szam1 ** szam2) # Hatványozás 5^10
print(szam3 % szam1) # Maradékos osztás. Kiírja a maradékot az osztás elvégzése után!
